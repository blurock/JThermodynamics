package thermo.data.structure.structure.symmetry;

import java.util.ArrayList;
import java.util.Iterator;

/**
 *
 * @author blurock
 */
public class ListOfSymmetryPairs extends ArrayList<SymmetryPair> {

    @Override
    public String toString() {
        StringBuffer buf = new StringBuffer();
        
        Iterator<SymmetryPair> ipair = this.iterator();
        while(ipair.hasNext()) {
            SymmetryPair pair = ipair.next();
            buf.append(pair.toString() + "\t");
        }
        
        return buf.toString();
    }
}
