    /**
     * Test of toCML method, of class CMLBensonRedundantBondsGroups.
     */
    @Test
    public void testToAndFromCML() {
        try {
            System.out.println("toCML");
            CMLNameListSet instance = new CMLNameListSet();
            NameListSet groups = new NameListSet("TestSet");
            String[] names = new String[3];
            names[0] = "Name1";
            names[1] = "Name2";
            names[2] = "Name3";
            groups.add(names[0]);
            groups.add(names[1]);
            groups.add(names[2]);

            System.out.println("===== Groups with 3 names =======");
            System.out.println(groups.toString());
            instance.setStructure(groups);
            String cml = instance.restore();

            CMLNameListSet instance1 = new CMLNameListSet();
            instance1.parse(cml);
            NameListSet group1 = (NameListSet) instance1.getStructure();

            System.out.println("===== From CML Groups with 3 names =======");
            System.out.println(group1.toString());
            
        } catch (ValidityException ex) {
            Logger.getLogger(CMLNameListSetTest.class.getName()).log(Level.SEVERE, null, ex);
        } catch (ParsingException ex) {
            Logger.getLogger(CMLNameListSetTest.class.getName()).log(Level.SEVERE, null, ex);
        } catch (IOException ex) {
            Logger.getLogger(CMLNameListSetTest.class.getName()).log(Level.SEVERE, null, ex);
        }
    }


